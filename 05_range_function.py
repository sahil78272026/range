# range used to print sequence of numbers
for num in range(8):  # will print number from 0 to 7
    print(num)

print ("\n")

for num2 in range(2,8): # will print number from 2 to 7 , 
    print(num2)

print ("\n")

for num3 in range(1,8,2):  # 2 is step size , will print numbers from 1 to 7 and skip two numbers eg, 1,3,5,7
    print(num3)
